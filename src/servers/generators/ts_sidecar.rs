//! Long-tail half of the OF-014 hybrid: ontogen writes a small Rust binary
//! into the user's crate that uses `specta::TypeCollection` +
//! `specta_typescript::Typescript` to emit TS for user-owned types referenced
//! by custom API endpoints. The build script then invokes the binary via
//! cargo with a distinct `CARGO_TARGET_DIR` (avoids the parent cargo's
//! target-dir lock — see rust-lang/cargo#8938) and captures stdout.
//!
//! Spike-grade. Hardcodes a few things a productionized version would derive
//! from `cargo metadata` and the `ServersConfig`.

use std::path::{Path, PathBuf};
use std::process::Command;

const SIDECAR_BIN_NAME: &str = "__ontogen_ts_export";

/// Generate the side-car binary source file. `lib_crate_name` is the name
/// the user's lib is exposed as (e.g., `iron_log`); `types_module_path` is
/// the module path inside the lib where the long-tail types live (e.g.,
/// `schema` — the binary will reference `${lib_crate_name}::${types_module_path}::${type_name}`).
///
/// Dead code as of OF-015 PR 5 (ontogen-ts replaced the side-car). PR 6
/// removes this whole module.
#[allow(dead_code)]
pub fn generate_sidecar_source(lib_crate_name: &str, types_module_path: &str, long_tail: &[String]) -> String {
    let mut out = String::new();
    out.push_str("//! Auto-generated by ontogen. DO NOT EDIT.\n");
    out.push_str("//! OF-014 spike: emits TypeScript for the user-owned long-tail\n");
    out.push_str("//! types referenced by custom API endpoints.\n\n");
    out.push_str("fn main() {\n");
    out.push_str("    let mut types = specta::TypeCollection::default();\n");
    for t in long_tail {
        out.push_str(&format!("    types.register_mut::<{lib_crate_name}::{types_module_path}::{t}>();\n"));
    }
    // `BigIntExportBehavior::Number` mirrors what the schema-known emitter
    // does for `i64`/`u64` (TS `number`). Without this, specta refuses to
    // export i64 fields by default. For the spike this is the right call;
    // productionization may want to plumb the choice through ServersConfig.
    out.push_str(
        "    let cfg = specta_typescript::Typescript::default()\n\
        \x20       .bigint(specta_typescript::BigIntExportBehavior::Number);\n",
    );
    out.push_str("    match cfg.export(&types) {\n");
    out.push_str("        Ok(ts) => print!(\"{}\", ts),\n");
    out.push_str("        Err(e) => {\n");
    out.push_str("            eprintln!(\"ontogen ts_export side-car failed: {e}\");\n");
    out.push_str("            std::process::exit(1);\n");
    out.push_str("        }\n");
    out.push_str("    }\n");
    out.push_str("}\n");
    out
}

/// Write the side-car source to the user's crate at `src/bin/<bin>.rs`.
/// Returns the path written.
///
/// Dead code as of OF-015 PR 5. PR 6 removes this whole module.
#[allow(dead_code)]
pub fn write_sidecar_source(manifest_dir: &Path, source: &str) -> PathBuf {
    let bin_dir = manifest_dir.join("src/bin");
    std::fs::create_dir_all(&bin_dir).expect("Failed to create src/bin for ontogen ts_export side-car");
    let path = bin_dir.join(format!("{SIDECAR_BIN_NAME}.rs"));
    std::fs::write(&path, source).expect("Failed to write ontogen ts_export side-car source");
    path
}

/// Build + run the side-car via cargo, isolated to `target_dir` so we don't
/// fight the outer cargo's target-dir lock. Returns captured stdout (the TS
/// to splice into bindings.ts).
///
/// Dead code as of OF-015 PR 5. PR 6 removes this whole module.
#[allow(dead_code)]
pub fn run_sidecar(manifest_dir: &Path, target_dir: &Path) -> Result<String, String> {
    let manifest_path = manifest_dir.join("Cargo.toml");
    let output = Command::new(std::env::var("CARGO").unwrap_or_else(|_| "cargo".to_string()))
        .arg("run")
        .arg("--quiet")
        .arg("--bin")
        .arg(SIDECAR_BIN_NAME)
        .arg("--manifest-path")
        .arg(&manifest_path)
        .env("CARGO_TARGET_DIR", target_dir)
        // Break recursion: when the inner cargo re-runs the user's
        // build.rs, ontogen sees this var and skips the side-car block
        // (which would otherwise spawn another inner cargo... forever).
        .env("ONTOGEN_TS_SIDECAR_INNER", "1")
        // Strip CARGO_* env vars the outer cargo set, except CARGO itself.
        // These can otherwise carry build profile / target settings into the
        // inner invocation and trigger the lock contention we're trying to
        // avoid.
        .env_remove("CARGO_MANIFEST_DIR")
        .env_remove("CARGO_PKG_NAME")
        .env_remove("CARGO_PKG_VERSION")
        .env_remove("CARGO_BIN_NAME")
        .output()
        .map_err(|e| format!("failed to spawn ontogen ts_export side-car: {e}"))?;
    if !output.status.success() {
        let stderr = String::from_utf8_lossy(&output.stderr);
        return Err(format!("ontogen ts_export side-car failed (status {}): {stderr}", output.status));
    }
    String::from_utf8(output.stdout).map_err(|e| format!("ts_export side-car stdout was not UTF-8: {e}"))
}

/// Append `ts` to `bindings_path`, with a separator comment.
pub fn append_to_bindings(bindings_path: &Path, ts: &str) -> Result<(), String> {
    let mut existing = std::fs::read_to_string(bindings_path).unwrap_or_default();
    existing.push_str("\n// Long-tail types (emitted via ontogen ts_export side-car).\n");
    existing.push_str(ts);
    if !existing.ends_with('\n') {
        existing.push('\n');
    }
    std::fs::write(bindings_path, existing).map_err(|e| format!("failed to append to {}: {e}", bindings_path.display()))
}
