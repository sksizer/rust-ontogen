#[cfg(test)]
mod tests {
    use super::*;
    use crate::schema::model::*;

    fn make_test_entities() -> Vec<EntityDef> {
        vec![
            EntityDef {
                name: "Node".to_string(),
                directory: "node".to_string(),
                table: "nodes".to_string(),
                type_name: "node".to_string(),
                prefix: "node".to_string(),
                fields: vec![
                    FieldDef::new("id", FieldType::String, FieldRole::Id),
                    FieldDef::new("name", FieldType::String, FieldRole::Plain),
                    FieldDef::new(
                        "parent_id",
                        FieldType::OptionString,
                        FieldRole::Relation(RelationInfo {
                            kind: RelationKind::BelongsTo,
                            target: "Node".to_string(),
                            junction: None,
                            foreign_key: None,
                        }),
                    ),
                    FieldDef::new(
                        "contains",
                        FieldType::VecString,
                        FieldRole::Relation(RelationInfo {
                            kind: RelationKind::HasMany,
                            target: "Node".to_string(),
                            junction: None,
                            foreign_key: Some("parent_id".to_string()),
                        }),
                    ),
                    FieldDef::new(
                        "fulfills",
                        FieldType::VecString,
                        FieldRole::Relation(RelationInfo {
                            kind: RelationKind::ManyToMany,
                            target: "Requirement".to_string(),
                            junction: None,
                            foreign_key: None,
                        }),
                    ),
                    FieldDef::new("body", FieldType::String, FieldRole::Body),
                ],
            },
            EntityDef {
                name: "Contract".to_string(),
                directory: "contract".to_string(),
                table: "contracts".to_string(),
                type_name: "contract".to_string(),
                prefix: "contract".to_string(),
                fields: vec![
                    FieldDef::new("id", FieldType::String, FieldRole::Id),
                    FieldDef::new("contract_type", FieldType::Other("ContractType".to_string()), FieldRole::EnumField),
                    FieldDef::new("body", FieldType::String, FieldRole::Body),
                ],
            },
        ]
    }

    #[test]
    fn test_ontology_element_enum() {
        let code = generate_parser_dispatch(&make_test_entities());
        assert!(code.contains("Node(Node),"), "Node should use schema type");
        assert!(code.contains("Contract(Contract),"), "Contract should use schema type");
    }

    #[test]
    fn test_parse_result_struct() {
        let code = generate_parser_dispatch(&make_test_entities());
        assert!(code.contains("pub nodes: Vec<Parsed<Node>>"));
        assert!(code.contains("pub contracts: Vec<Parsed<Contract>>"));
        assert!(code.contains("pub warnings: Vec<ParseWarning>"));
    }

    #[test]
    fn test_deserialize_function() {
        let code = generate_parser_dispatch(&make_test_entities());
        assert!(code.contains("\"node\" =>"));
        assert!(code.contains("let v: Node ="));
        assert!(code.contains("\"contract\" =>"));
        assert!(code.contains("let v: Contract ="));
        assert!(code.contains("OntologyElement::Node(v)"));
        assert!(code.contains("OntologyElement::Contract(v)"));
    }

    #[test]
    fn test_push_parsed_wikilink_stripping() {
        let code = generate_parser_dispatch(&make_test_entities());
        // BelongsTo optional: strip_wikilink_opt
        assert!(code.contains("strip_wikilink_opt(entity.parent_id.take())"));
        // HasMany: strip_wikilinks_vec with mem::take
        assert!(code.contains("strip_wikilinks_vec(std::mem::take(&mut entity.contains))"));
        // ManyToMany: strip_wikilinks_vec with mem::take
        assert!(code.contains("strip_wikilinks_vec(std::mem::take(&mut entity.fulfills))"));
    }

    #[test]
    fn test_push_parsed_prefix_check() {
        let code = generate_parser_dispatch(&make_test_entities());
        assert!(code.contains(r#"starts_with("node-")"#));
    }

    #[test]
    fn test_push_parsed_body_assignment() {
        let code = generate_parser_dispatch(&make_test_entities());
        assert!(code.contains("entity.body = body;"));
    }

    #[test]
    fn test_parse_result_field_names() {
        assert_eq!(parse_result_field_name("node"), "nodes");
        assert_eq!(parse_result_field_name("work_session"), "sessions");
        assert_eq!(parse_result_field_name("evidence"), "evidence");
        assert_eq!(parse_result_field_name("contract"), "contracts");
    }

    #[test]
    fn generate_all_real_schemas() {
        let schema_dir = std::path::Path::new(env!("CARGO_MANIFEST_DIR")).join("../../src-tauri/src/schema");
        if !schema_dir.exists() {
            eprintln!("Skipping: schema dir not found");
            return;
        }

        let entities = crate::schema::parse::parse_schema_dir(&schema_dir).unwrap();
        assert!(!entities.is_empty());

        let code = generate_parser_dispatch(&entities);

        // Every entity should have an OntologyElement variant
        for entity in &entities {
            assert!(code.contains(&format!("{}(", entity.name)), "Missing OntologyElement variant for {}", entity.name);
        }

        // All entities should use schema type directly in OntologyElement
        for entity in &entities {
            let name = &entity.name;
            assert!(
                code.contains(&format!("{name}({name}),")),
                "{name} should use schema type directly in OntologyElement"
            );
        }

        // ParseResult should have a field for every entity
        for entity in &entities {
            let snake = to_snake_case(&entity.name);
            let field = parse_result_field_name(&snake);
            assert!(
                code.contains(&format!("pub {field}: Vec<Parsed<{}>>", entity.name)),
                "Missing ParseResult field for {}",
                entity.name
            );
        }

        // Deserialize function should handle all entity types
        for entity in &entities {
            assert!(
                code.contains(&format!("\"{}\" =>", entity.type_name)),
                "Missing deserialize arm for {}",
                entity.name
            );
        }
    }
}
