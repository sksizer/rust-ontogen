//! Generate Create/Update DTO types from parsed ontology entity definitions.
//!
//! For each entity, generates:
//! - `Create{Entity}Input` — all fields for creating a new entity
//! - `Update{Entity}Input` — all fields wrapped in `Option<T>` for partial updates
//!
//! Generated DTOs are clean API-layer types:
//! - No persistence-only fields (wikilinks, source_file)
//! - Typed enums (not raw strings)
//! - Includes relation fields (has_many, many_to_many) as Vec<String> for API input
//! - Includes skip fields (entity-level data not in DB columns)

use std::fs;
use std::path::Path;

use crate::persistence::seaorm::gen_entity::to_snake_case;
use crate::schema::model::{EntityDef, FieldDef, FieldRole, FieldType};

// ─── Public API ──────────────────────────────────────────────────────────────

/// Generate DTO modules and write them to the output directory.
pub fn generate(entities: &[EntityDef], output_dir: &Path) -> Result<(), String> {
    fs::create_dir_all(output_dir).map_err(|e| format!("Failed to create output dir {}: {e}", output_dir.display()))?;

    // Clean stale files from previous entity names
    let expected: std::collections::HashSet<String> = entities
        .iter()
        .map(|e| format!("{}.rs", to_snake_case(&e.name)))
        .chain(std::iter::once("mod.rs".to_string()))
        .collect();
    crate::clean_generated_dir(output_dir, &expected);

    let mut mod_names: Vec<String> = Vec::new();

    for entity in entities {
        let mod_name = to_snake_case(&entity.name);
        let code = generate_dto_code(entity);
        let path = output_dir.join(format!("{mod_name}.rs"));
        crate::write_and_format(&path, &code).map_err(|e| format!("Failed to write {}: {e}", path.display()))?;
        mod_names.push(mod_name);
    }

    // Write mod.rs
    mod_names.sort();
    let mod_rs = generate_mod_rs(&mod_names);
    let path = output_dir.join("mod.rs");
    crate::write_and_format(&path, &mod_rs).map_err(|e| format!("Failed to write {}: {e}", path.display()))?;

    Ok(())
}

// ─── Code Generation ─────────────────────────────────────────────────────────

/// Generate the full DTO module for one entity.
fn generate_dto_code(entity: &EntityDef) -> String {
    let entity_name = &entity.name;
    let create_fields = generate_create_fields(entity);
    let update_fields = generate_update_fields(entity);
    let default_fns = generate_default_fns(entity);

    format!(
        r#"//! Generated by ontogen. DO NOT EDIT.

use schemars::JsonSchema;
use serde::Deserialize;

/// Input for creating a new {entity_name}.
#[derive(Debug, Clone, Deserialize, JsonSchema, specta::Type)]
pub struct Create{entity_name}Input {{
{create_fields}
}}

/// Input for updating an existing {entity_name}.
/// All fields are optional; only provided fields are updated.
#[derive(Debug, Clone, Deserialize, JsonSchema, specta::Type)]
pub struct Update{entity_name}Input {{
{update_fields}
}}
{default_fns}"#
    )
}

/// Generate fields for CreateXInput.
fn generate_create_fields(entity: &EntityDef) -> String {
    let mut lines = Vec::new();

    for field in &entity.fields {
        if let Some(line) = generate_create_field(field) {
            lines.push(line);
        }
    }

    lines.join("\n")
}

/// Generate a single field for CreateXInput.
fn generate_create_field(field: &FieldDef) -> Option<String> {
    let name = &field.name;

    // Persistence-only fields are never in DTOs
    if name == "wikilinks" || name == "source_file" {
        return None;
    }

    // Skip fields ARE included in DTOs (they're schema-level data, just not in DB columns)
    // Relation fields (has_many, many_to_many) ARE included as Vec<String> for API input
    let rust_type = field_to_create_type(field);
    let serde_attr = generate_serde_default_attr(field);
    Some(format!("{serde_attr}    pub {name}: {rust_type},"))
}

/// Generate fields for UpdateXInput.
fn generate_update_fields(entity: &EntityDef) -> String {
    let mut lines = Vec::new();

    for field in &entity.fields {
        if let Some(line) = generate_update_field(field) {
            lines.push(line);
        }
    }

    lines.join("\n")
}

/// Generate a single field for UpdateXInput.
fn generate_update_field(field: &FieldDef) -> Option<String> {
    let name = &field.name;

    // Persistence-only fields are never in DTOs
    if name == "wikilinks" || name == "source_file" {
        return None;
    }

    match &field.role {
        // ID is not updatable
        FieldRole::Id => None,
        // Skip fields, relation fields ARE included (same as create)
        _ => {
            let inner_type = field_to_create_type(field);
            let rust_type = format!("Option<{inner_type}>");
            Some(format!("    #[serde(default)]\n    pub {name}: {rust_type},"))
        }
    }
}

/// Map a field to its Rust type for the Create DTO.
fn field_to_create_type(field: &FieldDef) -> String {
    match &field.field_type {
        FieldType::String => "String".to_string(),
        FieldType::OptionString => "Option<String>".to_string(),
        FieldType::I32 => "i32".to_string(),
        FieldType::OptionI32 => "Option<i32>".to_string(),
        FieldType::VecString => "Vec<String>".to_string(),
        FieldType::VecStruct(t) => format!("Vec<crate::schema::{t}>"),
        FieldType::OptionEnum(t) => format!("Option<{}>", qualify_type(t)),
        FieldType::Other(t) => qualify_type(t),
    }
}

/// Qualify a type with `crate::schema::` unless it's a primitive.
fn qualify_type(t: &str) -> String {
    match t {
        "u8" | "u16" | "u32" | "u64" | "u128" | "usize" | "i8" | "i16" | "i32" | "i64" | "i128" | "isize" | "f32"
        | "f64" | "bool" | "char" | "String" => t.to_string(),
        _ => format!("crate::schema::{t}"),
    }
}

/// Generate the `#[serde(default)]` or `#[serde(default = "fn_name")]` attribute for a Create field.
fn generate_serde_default_attr(field: &FieldDef) -> String {
    match &field.role {
        FieldRole::Id => String::new(),
        _ => {
            let needs_default = field.serde_default
                || matches!(
                    field.field_type,
                    FieldType::OptionString
                        | FieldType::OptionI32
                        | FieldType::OptionEnum(_)
                        | FieldType::VecString
                        | FieldType::VecStruct(_)
                );

            if field.default_value.is_some() {
                // Generate serde(default = "fn_name") pointing to an inline function
                let fn_name = format!("default_{}", field.name);
                format!("    #[serde(default = \"{fn_name}\")]\n",)
            } else if needs_default {
                "    #[serde(default)]\n".to_string()
            } else {
                String::new()
            }
        }
    }
}

/// Generate default value helper functions that serde(default = "fn_name") references.
fn generate_default_fns(entity: &EntityDef) -> String {
    let mut fns = String::new();
    for field in &entity.fields {
        if let Some(ref default_val) = field.default_value {
            let fn_name = format!("default_{}", field.name);
            let rust_type = field_to_create_type(field);
            fns.push_str(&format!("\nfn {fn_name}() -> {rust_type} {{\n    \"{default_val}\".to_string()\n}}\n"));
        }
    }
    fns
}

/// Generate the `mod.rs` re-exporting all DTO modules.
fn generate_mod_rs(mod_names: &[String]) -> String {
    let mut out = String::from("//! Generated by ontogen. DO NOT EDIT.\n\n");
    for name in mod_names {
        out.push_str(&format!("pub mod {name};\npub use {name}::*;\n"));
    }
    out
}

