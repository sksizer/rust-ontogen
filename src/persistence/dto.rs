    #[test]
    fn no_wikilinks_or_source_file() {
        let schema_dir = std::path::Path::new(env!("CARGO_MANIFEST_DIR")).join("../../src-tauri/src/schema");
        if !schema_dir.exists() {
            eprintln!("Skipping: schema dir not found");
            return;
        }

        let entities = crate::schema::parse::parse_schema_dir(&schema_dir).expect("Failed to parse schema dir");

        let dir = std::env::temp_dir().join("ontology_codegen_test_dtos");
        let _ = std::fs::remove_dir_all(&dir);

        generate(&entities, &dir).expect("generate failed");

        // Check every generated DTO file
        for entry in std::fs::read_dir(&dir).unwrap() {
            let entry = entry.unwrap();
            let name = entry.file_name().to_string_lossy().to_string();
            if name == "mod.rs" {
                continue;
            }
            let code = std::fs::read_to_string(entry.path()).unwrap();
            assert!(!code.contains("wikilinks"), "{name} should not contain wikilinks");
            assert!(!code.contains("source_file"), "{name} should not contain source_file");
        }

        let _ = std::fs::remove_dir_all(&dir);
    }
