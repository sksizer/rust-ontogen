//! Generate CRUD forwarding functions for one entity.
//!
//! Each generated module delegates to the Store methods:
//! - `list` → `store.list_{entities}()`
//! - `get_by_id` → `store.get_{entity}(id)`
//! - `create` → `input.into()` + `store.create_{entity}(entity)`
//! - `update` → `input.into()` + `store.update_{entity}(id, updates)`
//! - `delete` → `store.delete_{entity}(id)`

use crate::ApiConfig;
use crate::schema::model::EntityDef;
use crate::store::helpers::{pluralize, to_snake_case};

/// Generate the complete API module source for one entity.
pub fn generate_crud_module(entity: &EntityDef, config: &ApiConfig) -> String {
    let name = &entity.name;
    let snake = to_snake_case(name);
    let plural = pluralize(&snake);
    let schema_path = &config.schema_module_path;

    let mut code = String::with_capacity(2048);

    code.push_str("//! Generated by ontogen. DO NOT EDIT.\n\n");

    // Imports — sorted to match rustfmt's ordering (sub-paths before parent-level imports)
    code.push_str(&format!("use {schema_path}::AppError;\n"));
    code.push_str(&format!("use {schema_path}::{name};\n"));
    code.push_str(&format!("use {schema_path}::{{Create{name}Input, Update{name}Input}};\n"));
    code.push_str(&format!("use crate::store::{snake}::{name}Update;\n"));
    code.push_str("use crate::store::Store;\n\n");
    code.push('\n');

    // list
    code.push_str(&format!("/// List all {plural}\n"));
    code.push_str(&format!("pub async fn list(store: &Store) -> Result<Vec<{name}>, AppError> {{\n"));
    code.push_str(&format!("    store.list_{plural}(None, None).await\n"));
    code.push_str("}\n\n");

    // get_by_id
    code.push_str(&format!("/// Get a single {snake} by ID\n"));
    code.push_str(&format!("pub async fn get_by_id(store: &Store, id: &str) -> Result<{name}, AppError> {{\n"));
    code.push_str(&format!("    store.get_{snake}(id).await\n"));
    code.push_str("}\n\n");

    // create
    code.push_str(&format!("/// Create a new {snake}\n"));
    code.push_str(&format!(
        "pub async fn create(store: &Store, input: Create{name}Input) -> Result<{name}, AppError> {{\n"
    ));
    code.push_str(&format!("    let {snake}: {name} = input.into();\n"));
    code.push_str(&format!("    store.create_{snake}({snake}).await\n"));
    code.push_str("}\n\n");

    // update
    code.push_str(&format!("/// Update an existing {snake}\n"));
    code.push_str(&format!(
        "pub async fn update(store: &Store, id: &str, input: Update{name}Input) -> Result<{name}, AppError> {{\n"
    ));
    code.push_str(&format!("    let updates: {name}Update = input.into();\n"));
    code.push_str(&format!("    store.update_{snake}(id, updates).await\n"));
    code.push_str("}\n\n");

    // delete
    code.push_str(&format!("/// Delete a {snake} by ID\n"));
    code.push_str("pub async fn delete(store: &Store, id: &str) -> Result<(), AppError> {\n");
    code.push_str(&format!("    store.delete_{snake}(id).await\n"));
    code.push_str("}\n");

    code
}

#[cfg(test)]
mod tests {
    use super::*;
    use crate::schema::model::{EntityDef, FieldDef, FieldRole, FieldType};

    fn make_role_entity() -> EntityDef {
        EntityDef {
            name: "Role".to_string(),
            directory: "role".to_string(),
            table: "roles".to_string(),
            type_name: "role".to_string(),
            prefix: "role".to_string(),
            fields: vec![
                FieldDef::new("id", FieldType::String, FieldRole::Id),
                FieldDef::new("body", FieldType::String, FieldRole::Body),
            ],
        }
    }

    fn make_config() -> ApiConfig {
        ApiConfig {
            output_dir: std::path::PathBuf::new(),
            exclude: vec![],
            scan_dirs: vec![],
            state_type: "AppState".to_string(),
            store_type: Some("Store".to_string()),
            schema_module_path: "crate::schema".to_string(),
        }
    }

    /// Syntax check: verify `generate_crud_module` emits a syntactically valid
    /// Rust source file.
    #[test]
    fn generated_code_is_valid_rust() {
        let config = make_config();
        let code = generate_crud_module(&make_role_entity(), &config);
        syn::parse_file(&code).unwrap_or_else(|e| {
            panic!("api::gen_crud::generate_crud_module produced invalid Rust: {e}\n--- code ---\n{code}")
        });
    }
}
